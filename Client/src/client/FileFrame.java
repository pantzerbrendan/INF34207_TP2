    private void fileChooserButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_fileChooserButtonActionPerformed
        // TODO add your handling code here:
        JFileChooser jfc = new JFileChooser(new File("."));
        File openedFile;

        if (jfc.showOpenDialog(null) == JFileChooser.APPROVE_OPTION) {
            openedFile = jfc.getSelectedFile();
            String filename = openedFile.getName();
            ListModel<String> model = this.filesList.getModel();
            DefaultListModel listmodel = new DefaultListModel();
            for (int i = 0; i < model.getSize(); i++) {
                listmodel.addElement(model.getElementAt(i));
            }
            listmodel.addElement(filename);
            this.filesList.setModel(listmodel);

            BufferedReader br = null;

            try {
                br = new BufferedReader(new FileReader(openedFile.toString()));
                StringBuilder sb = new StringBuilder();
                String line = br.readLine();

                while (line != null) {
                    sb.append(line);
                    sb.append(System.lineSeparator());
                    line = br.readLine();
                }
                String content = sb.toString();
                PrintWriter wr = new PrintWriter(this.socket.getOutputStream());
                wr.print("FILE_NAME = " + filename);
                wr.print(content);
            } catch (Exception ex) {
                Logger.getLogger(FileFrame.class.getName()).log(Level.SEVERE, null, ex);
            } finally {
                try {
                    br.close();
                } catch (IOException ex) {
                    Logger.getLogger(FileFrame.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_fileChooserButtonActionPerformed

